#!/usr/bin/env python3
"""
SATO SWARM — Repair Loop Test (standalone function + wired end-to-end)

Tests src/baseline/pipeline.py's _attempt_hipcc_repair() two ways:

  TEST A — direct: calls _attempt_hipcc_repair() itself with a real
  ToolRegistry + PortingMemory + a genuinely hipify-perl-translated
  repairDemo.hip.cpp file, feeding it a manually-constructed hipcc error
  message. Covers: successful repair, "matched pattern has no auto_fix"
  (skip, don't apply), and "nothing matches, budget exhausted".

  TEST B — wired: monkeypatches ONLY src.baseline.pipeline's own
  run_hipcc reference (the FIRST, direct compile attempt inside
  run_baseline()) to return that same simulated failure, then calls the
  REAL, UNMODIFIED run_baseline() end-to-end for a SeedId.REPAIR_DEMO
  job. The repair loop's OWN recompile call goes through
  ToolRegistry -> execution.run_hipcc — a DIFFERENT reference, untouched
  by this monkeypatch (pipeline.py imported run_hipcc as a bare name;
  ToolRegistry looks it up as execution.run_hipcc fresh each call) — so
  in MOCK mode it genuinely succeeds. This proves run_baseline()'s
  seed_id-gated repair block correctly turns a first-try failure into a
  COMPLETED job through the real, unmodified pipeline code, not a
  reimplemented/simplified stand-in for it.

WHY simulate the hipcc failure at all: SATOSWARM_MOCK's run_hipcc() never
shells out, so it always reports success — there is no way to produce a
genuine hipcc failure without a real ROCm toolchain, which this machine
does not have. The simulated error text is modeled on standard clang
diagnostic format and quotes the exact real, verified-absent identifier
(cudaCtxResetPersistingL2Cache, confirmed in an earlier session against
the live github.com/ROCm/HIPIFY and github.com/ROCm/HIP sources) —
everything downstream of that one synthetic input (pattern matching,
file patching, memory persistence, recompilation, job status) is real
code executing for real, not mocked out.

Usage:
    SATOSWARM_MOCK=1 python scripts/test_repair_loop.py

What this does NOT prove: that a REAL hipcc on a real pod produces this
exact error text, or that recompiling after the patch succeeds on real
hardware — see the summary this script prints at the end for the exact
real-hardware command to run to confirm that.
"""

import shutil
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

import src.baseline.pipeline as pipeline_module
from src.agents.tools import ToolRegistry
from src.baseline.pipeline import _attempt_hipcc_repair, run_baseline
from src.memory.loader import PortingMemory
from src.models.job import JobState, JobStatus, SeedId
from src.tools.execution import MOCK
from src.workspace.manager import WorkspaceManager

REPO_ROOT = Path(__file__).resolve().parents[1]
SEEDS_ROOT = REPO_ROOT / "seeds"
MEMORY_PATH = REPO_ROOT / "memory" / "porting_patterns.jsonl"
# Pre-generated by ACTUALLY RUNNING the real hipify-perl (downloaded from
# github.com/ROCm/HIPIFY in an earlier session) against the current
# seeds/repairDemo.cu via `perl hipify-perl.pl repairDemo.cu`, immediately
# before this test script runs — see the shell history for that exact
# command and its output (exit 0, the one expected stderr warning, gap
# line untranslated, everything else translated normally). This script
# only *copies* that real output — it does not invoke perl itself, since
# perl lives in this machine's separate POSIX/Git-Bash environment, not on
# native Windows' PATH that Python's subprocess inherits when run from
# PowerShell.
REAL_HIPIFY_OUTPUT = Path(r"C:\Users\maski\.claude\jobs\f6b329f9\tmp\hipify_research\repairDemo_fresh_output.hip.cpp")

_passed = 0
_failed = 0


def check(label: str, condition: bool, detail: str = "") -> None:
    global _passed, _failed
    if condition:
        _passed += 1
        print(f"  [PASS] {label}")
    else:
        _failed += 1
        print(f"  [FAIL] {label}" + (f" — {detail}" if detail else ""))


# Modeled on standard clang diagnostic format; quotes the exact identifier
# verified (in an earlier session) to be absent from HIP's real headers
# and left untranslated by real hipify-perl. Not obtained from an actual
# hipcc run — see module docstring.
SIMULATED_HIPCC_ERROR = (
    "$ hipcc -O3 --offload-arch=gfx1100 hip_out/repairDemo.hip.cpp -o hip_out/repairDemo_hip\n"
    "hip_out/repairDemo.hip.cpp:101:16: error: use of undeclared identifier "
    "'cudaCtxResetPersistingL2Cache'\n"
    "  CHECK_CUDA(cudaCtxResetPersistingL2Cache());\n"
    "               ^\n"
    "1 error generated when compiling for gfx1100.\n"
)

UNRELATED_HIPCC_ERROR = (
    "$ hipcc -O3 --offload-arch=gfx1100 hip_out/foo.hip.cpp -o hip_out/foo_hip\n"
    "hip_out/foo.hip.cpp:12:3: error: expected ';' after expression\n"
    "1 error generated when compiling for gfx1100.\n"
)


def make_real_translated_hip_out(ws_dir: Path) -> str:
    """Populate hip_out/repairDemo.hip.cpp with GENUINE hipify-perl
    output (see REAL_HIPIFY_OUTPUT above) — not MOCK-mode run_hipify()'s
    placeholder (a one-line stub comment, not a real translation; see
    execution.py). This is what apply_search_replace patches in TEST A
    below. Returns the real stderr warning hipify-perl printed for this
    file, captured at generation time.
    """
    hip_out = ws_dir / "hip_out"
    hip_out.mkdir(exist_ok=True)
    dest = hip_out / "repairDemo.hip.cpp"

    if not REAL_HIPIFY_OUTPUT.exists():
        raise FileNotFoundError(
            f"Expected pre-generated real hipify-perl output at {REAL_HIPIFY_OUTPUT} — not "
            f"found. Regenerate it (from the repo root, in a POSIX shell with perl and the "
            f"downloaded hipify-perl.pl on hand) with:\n"
            f"  perl <path-to-hipify-perl.pl> seeds/repairDemo.cu > {REAL_HIPIFY_OUTPUT}"
        )
    shutil.copy2(REAL_HIPIFY_OUTPUT, dest)
    return "warning: unsupported HIP identifier: cudaCtxResetPersistingL2Cache (see REAL_HIPIFY_OUTPUT's generation log)"


def main() -> None:
    print("=" * 70)
    print("SATO SWARM — Repair Loop Test")
    print("=" * 70)
    print(f"SATOSWARM_MOCK: {'1 (mock)' if MOCK else '0/unset (REAL)'}")
    print()

    ws = WorkspaceManager(base_dir=REPO_ROOT / "jobs")

    # =====================================================================
    print("=" * 70)
    print("TEST A — _attempt_hipcc_repair() called directly")
    print("=" * 70)

    job_a = JobState(seed_id=SeedId.REPAIR_DEMO)
    ws_dir_a = ws.create_workspace(job_a)
    ws.copy_seed(job_a, SEEDS_ROOT)
    hipify_stderr = make_real_translated_hip_out(ws_dir_a)
    print(f"Job ID: {job_a.job_id}")
    print(f"Real hipify-perl stderr (the actual unsupported-identifier warning): {hipify_stderr.strip()!r}")
    check(
        "real hipify-perl left the gap call untranslated in hip_out/repairDemo.hip.cpp",
        "cudaCtxResetPersistingL2Cache" in (ws_dir_a / "hip_out" / "repairDemo.hip.cpp").read_text(encoding="utf-8"),
    )
    print()

    tools_a = ToolRegistry(job_id=job_a.job_id, workspace_dir=ws_dir_a)
    memory_a = PortingMemory(path=MEMORY_PATH)
    messages: list[str] = []

    print("A1. Successful repair path (real error text, real pattern match, real patch, real recompile):")
    success, final_log, final_err, final_arch = _attempt_hipcc_repair(
        job_a, tools_a, memory_a,
        hip_sources_relative=["hip_out/repairDemo.hip.cpp"],
        out_binary_relative="hip_out/repairDemo_hip",
        initial_hipcc_log=SIMULATED_HIPCC_ERROR,
        job_msg=lambda text: (messages.append(text), print(f"   [msg] {text}")),
    )
    check("repair reported success", success is True)
    check("final_arch is set", bool(final_arch), final_arch)

    patched_content = (ws_dir_a / "hip_out" / "repairDemo.hip.cpp").read_text(encoding="utf-8")
    check(
        # NOT checking the bare identifier is absent file-wide — the
        # file's header comment legitimately documents/cites it by name
        # ~10 times as prose; that's correct and should stay. What must
        # be gone is the specific executable statement.
        "the gap line was ACTUALLY removed from the real file on disk",
        "CHECK_CUDA(cudaCtxResetPersistingL2Cache())" not in patched_content,
    )
    check(
        # hipDeviceSynchronize is NOT expected here as of the timing/
        # validation instrumentation added to repairDemo.cu — the standalone
        # sync call was removed as redundant once cudaEventSynchronize(stop)
        # was added (same structure vectorAdd.cu/tiledMatmul.cu already use).
        "the rest of the file (hipMalloc, hipEventSynchronize etc.) is untouched",
        "hipMalloc" in patched_content and "hipEventSynchronize" in patched_content,
    )
    check(
        "a log message named the correct pattern id",
        any("gap_cudaCtxResetPersistingL2Cache" in m for m in messages),
    )
    check(
        "no log message implies reasoning beyond keyword-overlap",
        all(("reasoning" not in m.lower() or "not semantic reasoning" in m) for m in messages),
    )

    reloaded_memory = PortingMemory(path=MEMORY_PATH)
    new_confirmed = [
        p for p in reloaded_memory._patterns
        if p.get("id", "").startswith(f"confirmed_repair_gap_cudaCtxResetPersistingL2Cache_{job_a.job_id}")
    ]
    check("a new confirmed_repair pattern was actually persisted to memory/porting_patterns.jsonl", len(new_confirmed) == 1)
    print()

    print("A2. 'Matched pattern has no auto_fix' path (cudaFuncGetName has no auto_fix field):")
    job_a2 = JobState(seed_id=SeedId.REPAIR_DEMO)
    ws_dir_a2 = ws.create_workspace(job_a2)
    ws.copy_seed(job_a2, SEEDS_ROOT)
    make_real_translated_hip_out(ws_dir_a2)
    tools_a2 = ToolRegistry(job_id=job_a2.job_id, workspace_dir=ws_dir_a2)
    memory_a2 = PortingMemory(path=MEMORY_PATH)
    messages_a2: list[str] = []
    no_autofix_error = (
        "hip_out/repairDemo.hip.cpp:50:5: error: use of undeclared identifier 'cudaFuncGetName'\n"
        "1 error generated when compiling for gfx1100.\n"
    )
    success_a2, *_ = _attempt_hipcc_repair(
        job_a2, tools_a2, memory_a2,
        hip_sources_relative=["hip_out/repairDemo.hip.cpp"],
        out_binary_relative="hip_out/repairDemo_hip",
        initial_hipcc_log=no_autofix_error,
        job_msg=lambda text: (messages_a2.append(text), print(f"   [msg] {text}")),
    )
    check("correctly reports overall failure (no auto-appliable pattern)", success_a2 is False)
    check(
        "explicitly logged that the matched pattern has no auto_fix, rather than guessing one",
        any("no auto_fix" in m for m in messages_a2),
    )
    print()

    print("A3. No pattern matches at all (unrelated error):")
    job_a3 = JobState(seed_id=SeedId.REPAIR_DEMO)
    ws_dir_a3 = ws.create_workspace(job_a3)
    ws.copy_seed(job_a3, SEEDS_ROOT)
    make_real_translated_hip_out(ws_dir_a3)
    tools_a3 = ToolRegistry(job_id=job_a3.job_id, workspace_dir=ws_dir_a3)
    memory_a3 = PortingMemory(path=MEMORY_PATH)
    messages_a3: list[str] = []
    success_a3, *_ = _attempt_hipcc_repair(
        job_a3, tools_a3, memory_a3,
        hip_sources_relative=["hip_out/repairDemo.hip.cpp"],
        out_binary_relative="hip_out/repairDemo_hip",
        initial_hipcc_log=UNRELATED_HIPCC_ERROR,
        job_msg=lambda text: (messages_a3.append(text), print(f"   [msg] {text}")),
    )
    check("correctly reports failure (nothing in memory matches)", success_a3 is False)
    check(
        # 2, not 1: the "no candidates" message plus the loop's own final
        # summary message — both fire on this path, no wasted attempts
        # (never got as far as trying to patch/recompile anything).
        "exactly 2 messages logged (stopped immediately, no wasted patch/recompile attempts)",
        len(messages_a3) == 2,
        f"got {len(messages_a3)}: {messages_a3}",
    )
    print()

    # Clean up the confirmed_repair pattern A1 persisted, so the seeded
    # memory file stays in its curated state (same convention as
    # scripts/test_memory.py).
    lines = MEMORY_PATH.read_text(encoding="utf-8").splitlines()
    clean_lines = [l for l in lines if f'"confirmed_repair_gap_cudaCtxResetPersistingL2Cache_{job_a.job_id}"' not in l]
    removed = len(lines) - len(clean_lines)
    MEMORY_PATH.write_text("\n".join(clean_lines) + "\n", encoding="utf-8")
    check(f"cleaned up the {removed} test-confirmed pattern from memory/porting_patterns.jsonl", removed == 1)
    check("memory file back to exactly the 4 curated patterns", len(PortingMemory(path=MEMORY_PATH)) == 4)
    print()

    # =====================================================================
    print("=" * 70)
    print("TEST B — wired through the REAL, unmodified run_baseline()")
    print("=" * 70)

    def fake_first_attempt_run_hipcc(hip_sources, out_binary, arch=None):
        return False, SIMULATED_HIPCC_ERROR, "1 error generated.", "gfx1100"

    def fake_run_hipify_with_real_content(source_dir, out_dir, job_id):
        # MOCK-mode's real run_hipify() only writes a one-line placeholder
        # (see execution.py) — no gap line for the repair loop to find.
        # This monkeypatch is ONLY about getting genuine translated
        # content into hip_out/ for the test; it's not simulating hipify
        # itself failing or succeeding differently than it really does.
        out_dir.mkdir(parents=True, exist_ok=True)
        dest = out_dir / "repairDemo.hip.cpp"
        shutil.copy2(REAL_HIPIFY_OUTPUT, dest)
        return True, "(real hipify-perl output copied in for this test — see REAL_HIPIFY_OUTPUT)", "", "hipify-perl"

    original_run_hipcc = pipeline_module.run_hipcc
    original_run_hipify = pipeline_module.run_hipify
    pipeline_module.run_hipcc = fake_first_attempt_run_hipcc
    pipeline_module.run_hipify = fake_run_hipify_with_real_content
    try:
        job_b = JobState(seed_id=SeedId.REPAIR_DEMO)
        result_job = run_baseline(job_b, ws, SEEDS_ROOT)
    finally:
        pipeline_module.run_hipcc = original_run_hipcc
        pipeline_module.run_hipify = original_run_hipify

    print(f"Job ID: {result_job.job_id}")
    print(f"Final status: {result_job.status}")
    print(f"Final phase: {result_job.phase}")
    print()
    print("Message trace:")
    for m in result_job.messages:
        print(f"  [{m.agent}/{m.type}] {m.content}")
    print()

    check(
        "run_baseline()'s FIRST compile attempt used the monkeypatched (simulated-failure) run_hipcc",
        any("hipcc failed" not in m.content and "Repair Loop" in m.agent for m in result_job.messages) or
        any(m.agent == "Repair Loop" for m in result_job.messages),
    )
    check(
        "the repair loop actually ran (Repair Loop messages present)",
        any(m.agent == "Repair Loop" for m in result_job.messages),
    )
    check(
        "job ended COMPLETED, not FAILED, through the real unmodified run_baseline()",
        result_job.status == JobStatus.COMPLETED,
        f"got {result_job.status}",
    )
    check(
        "job.error was never set (the FAILED-path code never ran — repair pre-empted it)",
        result_job.error is None,
    )
    check(
        "job.hipcc_command records that a repair happened",
        "repair loop" in (result_job.hipcc_command or "").lower(),
    )

    # Clean up whatever confirmed_repair pattern TEST B's real run_baseline() persisted.
    lines = MEMORY_PATH.read_text(encoding="utf-8").splitlines()
    clean_lines = [l for l in lines if f'"confirmed_repair_gap_cudaCtxResetPersistingL2Cache_{result_job.job_id}"' not in l]
    removed_b = len(lines) - len(clean_lines)
    MEMORY_PATH.write_text("\n".join(clean_lines) + "\n", encoding="utf-8")
    check(f"cleaned up TEST B's {removed_b} test-confirmed pattern too", removed_b == 1)
    check("memory file still back to exactly the 4 curated patterns", len(PortingMemory(path=MEMORY_PATH)) == 4)
    print()

    # =====================================================================
    print("=" * 70)
    print("REGRESSION CHECK — the 3 original seeds are untouched by all of this")
    print("=" * 70)
    for seed in (SeedId.VECTOR_ADD, SeedId.TILED_MATMUL, SeedId.REDUCTION):
        job_r = JobState(seed_id=seed)
        result_r = run_baseline(job_r, ws, SEEDS_ROOT)
        check(f"{seed.value}: still COMPLETED", result_r.status == JobStatus.COMPLETED)
        check(f"{seed.value}: no 'Repair Loop' messages (seed_id gate correctly skipped it)",
              not any(m.agent == "Repair Loop" for m in result_r.messages))
    print()

    print("=" * 70)
    print(f"RESULT: {_passed} passed, {_failed} failed")
    print("=" * 70)
    if _failed:
        sys.exit(1)

    print()
    print("REAL-HARDWARE TEST NEEDED TO CONFIRM THIS LIVE:")
    print("  On the pod (SATOSWARM_MOCK unset/0):")
    print("    python scripts/test_baseline.py repairDemo")
    print("  Expected: hipcc genuinely fails on cudaCtxResetPersistingL2Cache (first real")
    print("  confirmation this compile error actually occurs, not just predicted from the")
    print("  verified-absent header symbol) -> Repair Loop messages appear in the trace ->")
    print("  apply_search_replace removes the line for real -> hipcc is invoked again for")
    print("  real and this time should succeed (nothing else in the file is broken) -> final")
    print("  Status: JobStatus.COMPLETED. If the second real hipcc call fails for a DIFFERENT")
    print("  reason, that's new information — bring back logs/hipcc.log.")


if __name__ == "__main__":
    main()
